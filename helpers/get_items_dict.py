def get_items_dict(contractor_colspan):
    """
    Возвращает шаблонный словарь для представления одной позиции (строки) работ
    или материалов, включая специфичные для подрядчика поля.

    Структура возвращаемого словаря состоит из общей части, описывающей позицию
    (например, "порядковый номер", "наименование работ"), и части с данными
    подрядчика, которая варьируется в зависимости от параметра `contractor_colspan`.
    Все значения в шаблонном словаре по умолчанию установлены в `None`.

    Эта функция используется для создания базовой структуры, которая затем
    заполняется фактическими данными при парсинге.

    Args:
        contractor_colspan (int): Количество столбцов (colspan), которое занимают
                                   данные конкретного подрядчика на листе Excel.
                                   Это значение определяет набор полей, относящихся
                                   к подрядчику, которые будут включены в словарь.

    Returns:
        dict: Словарь-шаблон, представляющий одну позицию. Содержит общие поля
              и поля, специфичные для подрядчика, зависящие от `contractor_colspan`.
              Если передан неизвестный `contractor_colspan`, словарь будет содержать
              ключ "error".

    Структура возвращаемого словаря:

    Общие поля (всегда присутствуют):
    - "порядковый номер": None
    - "номер раздела": None
    - "статья смр": None
    - "наименование работ": None
    - "комментарий": None
    - "единица измерения": None
    - "количество": None

    Поля, зависящие от `contractor_colspan` (добавляются к общим полям):

    Если `contractor_colspan` == 12:
    - "предлагаемое количество": None
    - "стоимость за единицу": {"материалы": None, "работы": None, "косвенные расходы": None, "всего": None}
    - "стоимость всего": {"материалы": None, "работы": None, "косвенные расходы": None, "всего": None}
    - "стоимость всего за объемы заказчика": None
    - "комментарий участника": None
    - "отклонение от расчетной стоимости": None

    Если `contractor_colspan` == 11:
    - "предлагаемое количество": None
    - "стоимость за единицу": {"материалы": None, "работы": None, "косвенные расходы": None, "всего": None}
    - "стоимость всего": {"материалы": None, "работы": None, "косвенные расходы": None, "всего": None}
    - "стоимость всего за объемы заказчика": None
    - "отклонение от расчетной стоимости": None

    Если `contractor_colspan` == 10:
    - "стоимость за единицу": {"материалы": None, "работы": None, "косвенные расходы": None, "всего": None}
    - "стоимость всего": {"материалы": None, "работы": None, "косвенные расходы": None, "всего": None}
    - "комментарий участника": None
    - "отклонение от расчетной стоимости": None

    Если `contractor_colspan` == 9:
    - "стоимость за единицу": {"материалы": None, "работы": None, "косвенные расходы": None, "всего": None}
    - "стоимость всего": {"материалы": None, "работы": None, "косвенные расходы": None, "всего": None}
    - "отклонение от расчетной стоимости": None

    Если `contractor_colspan` == 8:
    - "стоимость за единицу": {"материалы": None, "работы": None, "косвенные расходы": None, "всего": None}
    - "стоимость всего": {"материалы": None, "работы": None, "косвенные расходы": None, "всего": None}

    Если `contractor_colspan` имеет другое значение:
    - "error": "Unknown contractor_colspan: {значение contractor_colspan}"
               (и также будут присутствовать общие поля).
    """
    item = {
        "порядковый номер": None,
        "номер раздела": None,
        "статья смр": None,
        "наименование работ": None,
        "комментарий": None,
        "единица измерения": None,
        "количество": None
    }

    # Шаблон для блоков стоимости
    unit_block = {
        "материалы": None,
        "работы": None,
        "косвенные расходы": None,
        "всего": None
    }

    contractor_data = {}
    if contractor_colspan == 12:
        contractor_data = {
            "предлагаемое количество": None,
            "стоимость за единицу": unit_block.copy(),  # .copy() для независимых словарей
            "стоимость всего": unit_block.copy(),
            "стоимость всего за объемы заказчика": None,
            "комментарий участника": None,
            "отклонение от расчетной стоимости": None
        }
    elif contractor_colspan == 11:
        contractor_data = {
            "предлагаемое количество": None,
            "стоимость за единицу": unit_block.copy(),
            "стоимость всего": unit_block.copy(),
            "стоимость всего за объемы заказчика": None,
            "отклонение от расчетной стоимости": None
        }
    elif contractor_colspan == 10:
        contractor_data = {
            "стоимость за единицу": unit_block.copy(),
            "стоимость всего": unit_block.copy(),
            "комментарий участника": None,
            "отклонение от расчетной стоимости": None
        }
    elif contractor_colspan == 9:
        contractor_data = {
            "стоимость за единицу": unit_block.copy(),
            "стоимость всего": unit_block.copy(),
            "отклонение от расчетной стоимости": None
        }
    elif contractor_colspan == 8:
        contractor_data = {
            "стоимость за единицу": unit_block.copy(),
            "стоимость всего": unit_block.copy()
        }
    else:
        # Обработка неизвестного значения colspan
        contractor_data = {"error": f"Unknown contractor_colspan: {contractor_colspan}"}

    # Объединяем общую часть с частью подрядчика
    return {**item, **contractor_data}